public class Math {
    public static int sum (int x, int y){
        return x + y;

    }
    public static int mult (int x, int y){
        return x * y;
    }
    public static int  subs (int x, int y){
        return x - y;

    }
    public static float div (int x, int y){
        return (float)x / y;

    }

}
